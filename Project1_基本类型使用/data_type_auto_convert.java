package Project1_基本类型使用;
/*1.自动类型转换
特点：不需要特殊处理，自动完成
规则：数据类型从小到大转换
  2.强制类型转换
 */
public class data_type_auto_convert {
    public static void main(String[] args) {
        long num1 = 100;
        System.out.println(num1);
        
        double num2 = 2.5F;
        System.out.println(num2);

        float num3 = 200L;
        System.out.println(num3);
    }
}
